#!/usr/bin/env python3
"""SSRM-3D learned-controller precursor.

The recurrent-observer precursor showed that self-state can be recovered from
embodied traces. This follow-up asks whether a learned policy state, trained
without self labels, also recovers self-equivalent variables when it must choose
actions in the SSRM-3D world.

The learner is deliberately narrow: it uses return-weighted behavior cloning and
future-return prediction from traces generated by the existing SSRM-3D agents.
It is a learned controller precursor, not a solved RL agent and not a
consciousness experiment. The LLM remains absent from the learned policy; fast
reflex overrides still handle immediate hazard avoidance.
"""

