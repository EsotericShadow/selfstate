class PolicyNet(nn.Module):
    def __init__(self, architecture: str, input_size: int, hidden_size: int) -> None:
        super().__init__()
        self.architecture = architecture
        self.hidden_size = hidden_size
        if architecture == "frame_mlp":
            self.encoder = nn.Sequential(
                nn.Linear(input_size, hidden_size),
                nn.Tanh(),
                nn.Linear(hidden_size, hidden_size),
                nn.Tanh(),
            )
        elif architecture == "torch_rnn":
            self.recurrent = nn.RNN(input_size, hidden_size, batch_first=True, nonlinearity="tanh")
        elif architecture == "torch_gru":
            self.recurrent = nn.GRU(input_size, hidden_size, batch_first=True)
        elif architecture == "torch_lstm":
            self.recurrent = nn.LSTM(input_size, hidden_size, batch_first=True)
        else:
            raise ValueError(f"unknown architecture: {architecture}")
        self.policy_head = nn.Linear(hidden_size, len(MODES))
        self.value_head = nn.Linear(hidden_size, 1)

    def encode(self, inputs: torch.Tensor) -> torch.Tensor:
        if self.architecture == "frame_mlp":
            return self.encoder(inputs)
        outputs, _hidden = self.recurrent(inputs)
        return outputs

    def forward(self, inputs: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        hidden = self.encode(inputs)
        return hidden, self.policy_head(hidden), self.value_head(hidden).squeeze(-1)

    def initial_state(self, batch_size: int, device: torch.device) -> Optional[torch.Tensor | Tuple[torch.Tensor, torch.Tensor]]:
        if self.architecture == "frame_mlp":
            return None
        if self.architecture == "torch_lstm":
            zeros = torch.zeros((1, batch_size, self.hidden_size), dtype=torch.float32, device=device)
            return zeros, zeros.clone()
        return torch.zeros((1, batch_size, self.hidden_size), dtype=torch.float32, device=device)

    def step(
        self,
        input_row: torch.Tensor,
        state: Optional[torch.Tensor | Tuple[torch.Tensor, torch.Tensor]],
    ) -> Tuple[torch.Tensor, torch.Tensor, Optional[torch.Tensor | Tuple[torch.Tensor, torch.Tensor]]]:
        if self.architecture == "frame_mlp":
            hidden = self.encoder(input_row)
            return hidden, self.policy_head(hidden), None
        output, next_state = self.recurrent(input_row.unsqueeze(1), state)
        hidden = output[:, -1, :]
        return hidden, self.policy_head(hidden), next_state


def resolve_device(requested: str) -> torch.device:
    if requested == "auto":
        if torch.backends.mps.is_available():
            return torch.device("mps")
        return torch.device("cpu")
    if requested == "mps":
        if not torch.backends.mps.is_available():
            raise SystemExit("requested MPS device, but torch.backends.mps.is_available() is false")
        return torch.device("mps")
    if requested == "cpu":
        return torch.device("cpu")
    raise SystemExit("--device must be one of auto, mps, cpu")


def mode_one_hot(mode: str) -> List[float]:
    return [1.0 if mode == item else 0.0 for item in MODES]


def closeness(value: float, world_size: float) -> float:
    if math.isinf(value):
        return 0.0
    return ssrm.clamp(1.0 - value / max(world_size, EPS), 0.0, 1.0)


def object_distance(
    x: float,
    z: float,
    objects: Sequence[ssrm.WorldObject],
    active_ids: Optional[set[str]] = None,
) -> float:
    distances = []
    for obj in objects:
        if active_ids is not None and obj.id not in active_ids:
            continue
        if not obj.active:
            continue
        distances.append(ssrm.distance(x, z, obj.x, obj.z))
    return min(distances) if distances else float("inf")


def world_active_resource_ids(world: Dict[str, object]) -> set[str]:
    return {resource.id for resource in world["resources"] if resource.active}


def feature_row(
    state: ssrm.PhysicalState,
    packet: ssrm.PerceptionPacket,
    world: Dict[str, object],
    stage: ssrm.StageSpec,
    tick: int,
    cfg: LearnedControllerConfig,
    previous_x: float,
    previous_z: float,
    previous_mode: str,
) -> Tuple[List[float], List[float]]:
    active_ids = world_active_resource_ids(world)
    resources: Sequence[ssrm.WorldObject] = world["resources"]
    hazards: Sequence[ssrm.WorldObject] = world["hazards"]
    shelters: Sequence[ssrm.WorldObject] = world["shelters"]
    resource_distance = object_distance(state.x, state.z, resources, active_ids)
    hazard_distance = object_distance(state.x, state.z, hazards)
    shelter_distance = object_distance(state.x, state.z, shelters)
    competitor = world["competitor"]
    competitor_distance = (
        ssrm.distance(state.x, state.z, float(competitor["x"]), float(competitor["z"]))
        if bool(competitor["active"])
        else float("inf")
    )
    dx = (state.x - previous_x) / max(cfg.world_size, EPS)
    dz = (state.z - previous_z) / max(cfg.world_size, EPS)
    features = [
        state.x / (cfg.world_size * 0.5),
        state.z / (cfg.world_size * 0.5),
        dx * 15.0,
        dz * 15.0,
        min(1.0, math.hypot(dx, dz) * 75.0),
        math.sin(state.heading),
        math.cos(state.heading),
        ssrm.clamp((ssrm.terrain_height(state.x, state.z) + 2.0) / 4.0, 0.0, 1.0),
        closeness(resource_distance, cfg.world_size),
        closeness(hazard_distance, cfg.world_size),
        closeness(shelter_distance, cfg.world_size),
        packet.daylight,
        packet.weather,
        len(active_ids) / max(len(resources), 1),
        state.resources_collected / max(len(resources), 1),
        closeness(competitor_distance, cfg.world_size),
        packet.noisy_energy,
        packet.noisy_integrity,
        stage.index / max(item.index for item in ssrm.STAGES),
        1.0 if stage.hidden_energy else 0.0,
        1.0 if stage.actuator_drift else 0.0,
        1.0 if stage.delayed_rewards else 0.0,
        1.0 if stage.commitments else 0.0,
        1.0 if stage.subsystem_conflict else 0.0,
        1.0 if stage.multiagent else 0.0,
    ]
    features.extend(mode_one_hot(previous_mode))
    world_targets = [
        closeness(resource_distance, cfg.world_size),
        closeness(hazard_distance, cfg.world_size),
        packet.daylight,
        packet.weather,
    ]
    return features, world_targets


def trace_record(
    trace: Dict[str, object],
    metrics: ssrm.EpisodeMetrics,
    stage: ssrm.StageSpec,
    agent: str,
    episode: int,
    cfg: LearnedControllerConfig,
) -> ControllerRecord:
    frames = trace["frames"]
    world_objects = {
        "resources": [ssrm.WorldObject(**resource) for resource in trace["world"]["resources"]],
        "hazards": [ssrm.WorldObject(**hazard) for hazard in trace["world"]["hazards"]],
        "shelters": [ssrm.WorldObject(**shelter) for shelter in trace["world"]["shelters"]],
        "competitor": {"x": 0.0, "z": 0.0, "active": stage.multiagent},
    }
    inputs: List[List[float]] = []
    action_targets: List[int] = []
    future_returns: List[float] = []
    self_targets: List[List[float]] = []
    previous_mode = "explore_reduce_uncertainty"
    previous_x = float(frames[0]["x"]) if frames else 0.0
    previous_z = float(frames[0]["z"]) if frames else 0.0
    final_reward = float(frames[-1]["reward"]) if frames else metrics.total_reward
    for frame in frames:
        active_ids = set(str(item) for item in frame["active_resources"])
        for resource in world_objects["resources"]:
            resource.active = resource.id in active_ids
        world_objects["competitor"] = dict(frame["competitor"])
        state = ssrm.PhysicalState(
            x=float(frame["x"]),
            z=float(frame["z"]),
            heading=float(frame["heading"]),
            energy=float(frame["energy"]),
            integrity=float(frame["integrity"]),
            true_mobility=float(frame["mobility"]),
            true_sensor=float(frame["sensor"]),
            reward=float(frame["reward"]),
            resources_collected=int(frame["resources_collected"]),
        )
        daylight = 0.5 + 0.5 * math.cos((int(frame["tick"]) / max(cfg.ticks - 1, 1)) * math.tau)
        weather = ssrm.clamp(stage.weather_amp * (0.55 + 0.45 * math.sin(int(frame["tick"]) * 0.017 + stage.index)), 0.0, 1.0)
        packet = ssrm.PerceptionPacket(
            nearest_resource=None,
            nearest_hazard=None,
            nearest_shelter=None,
            resource_distance=float("inf"),
            hazard_distance=float("inf"),
            shelter_distance=float("inf"),
            daylight=daylight,
            weather=weather,
            novelty=0.0,
            noisy_energy=float(frame["energy"]),
            noisy_integrity=float(frame["integrity"]),
            visible_count=0,
            competitor_distance=float("inf"),
        )
        features, _world_targets = feature_row(state, packet, world_objects, stage, int(frame["tick"]), cfg, previous_x, previous_z, previous_mode)
        mode = str(frame["mode"])
        inputs.append(features)
        action_targets.append(MODE_TO_INDEX[mode])
        future_returns.append(final_reward - float(frame["reward"]))
        self_targets.append(
            [
                float(frame["energy"]),
                float(frame["integrity"]),
                float(frame["mobility"]),
                float(frame["sensor"]),
            ]
        )
        previous_x = float(frame["x"])
        previous_z = float(frame["z"])
        previous_mode = mode
    return ControllerRecord(
        stage=stage.index,
        stage_name=stage.name,
        agent=agent,
        episode=episode,
        total_reward=metrics.total_reward,
        inputs=inputs,
        action_targets=action_targets,
        future_returns=future_returns,
        sample_weights=[1.0 for _ in inputs],
        self_targets=self_targets,
    )


def generate_records(cfg: LearnedControllerConfig) -> List[ControllerRecord]:
    records: List[ControllerRecord] = []
    base_cfg = ssrm.SSRM3DConfig(
        episodes=1,
        ticks=cfg.ticks,
        seed=cfg.seed,
        stage_min=cfg.stage_min,
        stage_max=cfg.stage_max,
        world_size=cfg.world_size,
        perception_hz=cfg.perception_hz,
        goal_hz=cfg.goal_hz,
        reasoning_hz=cfg.reasoning_hz,
        trace_stage=cfg.stage_max,
        trace_episode=0,
    )
    stages = [stage for stage in ssrm.STAGES if cfg.stage_min <= stage.index <= cfg.stage_max]
    for stage in stages:
        for episode in range(cfg.episodes_per_stage):
            agent = cfg.source_agents[episode % len(cfg.source_agents)]
            metrics, trace = ssrm.run_episode(stage, agent, episode, base_cfg, collect_trace=True)
            if trace is None:
                continue
            records.append(trace_record(trace, metrics, stage, agent, episode, cfg))
    weight_records_by_stage(records, cfg)
    return records


def weight_records_by_stage(records: Sequence[ControllerRecord], cfg: LearnedControllerConfig) -> None:
    for stage in sorted({record.stage for record in records}):
        stage_records = [record for record in records if record.stage == stage]
        rewards = [record.total_reward for record in stage_records]
        low = min(rewards)
        span = max(max(rewards) - low, EPS)
        for record in stage_records:
            normalized = (record.total_reward - low) / span
            weight = cfg.return_weight_floor + (1.0 - cfg.return_weight_floor) * normalized
            record.sample_weights = [weight for _ in record.inputs]


def split_indices(records: Sequence[ControllerRecord], cfg: LearnedControllerConfig) -> Tuple[List[int], List[int]]:
    rng = random.Random(cfg.seed + 53)
    train: List[int] = []
    test: List[int] = []
    for stage in sorted({record.stage for record in records}):
        indices = [index for index, record in enumerate(records) if record.stage == stage]
        rng.shuffle(indices)
        train_count = max(1, min(len(indices) - 1, round(len(indices) * cfg.train_fraction)))
        train.extend(indices[:train_count])
        test.extend(indices[train_count:])
    return train, test


def build_tensors(
    records: Sequence[ControllerRecord],
    device: torch.device,
) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
    max_len = max(len(record.inputs) for record in records)
    input_size = len(records[0].inputs[0])
    inputs = torch.zeros((len(records), max_len, input_size), dtype=torch.float32)
    actions = torch.zeros((len(records), max_len), dtype=torch.long)
    returns = torch.zeros((len(records), max_len), dtype=torch.float32)
    weights = torch.zeros((len(records), max_len), dtype=torch.float32)
    self_targets = torch.zeros((len(records), max_len, 4), dtype=torch.float32)
    mask = torch.zeros((len(records), max_len), dtype=torch.float32)
    all_returns = [value for record in records for value in record.future_returns]
    return_min = min(all_returns)
    return_span = max(max(all_returns) - return_min, EPS)
    for index, record in enumerate(records):
        length = len(record.inputs)
        inputs[index, :length] = torch.tensor(record.inputs, dtype=torch.float32)
        actions[index, :length] = torch.tensor(record.action_targets, dtype=torch.long)
        returns[index, :length] = torch.tensor([(value - return_min) / return_span for value in record.future_returns], dtype=torch.float32)
        weights[index, :length] = torch.tensor(record.sample_weights, dtype=torch.float32)
        self_targets[index, :length] = torch.tensor(record.self_targets, dtype=torch.float32)
        mask[index, :length] = 1.0
    return inputs.to(device), actions.to(device), returns.to(device), weights.to(device), self_targets.to(device), mask.to(device)


def masked_value_mse(prediction: torch.Tensor, target: torch.Tensor, mask: torch.Tensor, weights: torch.Tensor) -> torch.Tensor:
    effective = mask * weights
    return (((prediction - target) ** 2) * effective).sum() / (effective.sum() + EPS)


def weighted_policy_loss(logits: torch.Tensor, target: torch.Tensor, mask: torch.Tensor, weights: torch.Tensor) -> torch.Tensor:
    loss = nn.functional.cross_entropy(logits.reshape(-1, logits.shape[-1]), target.reshape(-1), reduction="none").reshape(target.shape)
    effective = mask * weights
    return (loss * effective).sum() / (effective.sum() + EPS)


def train_model(
    architecture: str,
    cfg: LearnedControllerConfig,
    tensors: Tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor],
    train_indices: Sequence[int],
    device: torch.device,
) -> PolicyNet:
    inputs, actions, returns, weights, _self_targets, mask = tensors
    torch.manual_seed(cfg.seed + 911 * (ARCHITECTURES.index(architecture) + 1))
    model = PolicyNet(architecture, inputs.shape[-1], cfg.hidden_size).to(device)
    optimizer = torch.optim.AdamW(model.parameters(), lr=cfg.learning_rate, weight_decay=1e-4)
    train_indices_tensor = torch.tensor(list(train_indices), dtype=torch.long, device=device)
    generator = torch.Generator(device="cpu")
    generator.manual_seed(cfg.seed + 353 * (ARCHITECTURES.index(architecture) + 1))
    for _epoch in range(cfg.epochs):
        permutation = torch.randperm(len(train_indices), generator=generator).tolist()
        for start in range(0, len(permutation), cfg.batch_size):
            batch_positions = permutation[start : start + cfg.batch_size]
            batch = train_indices_tensor[torch.tensor(batch_positions, dtype=torch.long, device=device)]
            optimizer.zero_grad(set_to_none=True)
            _hidden, logits, value = model(inputs[batch])
            batch_mask = mask[batch]
            policy_loss = weighted_policy_loss(logits, actions[batch], batch_mask, weights[batch])
            value_loss = masked_value_mse(value, returns[batch], batch_mask, weights[batch])
            loss = policy_loss + cfg.value_weight * value_loss
            loss.backward()
            torch.nn.utils.clip_grad_norm_(model.parameters(), 2.5)
            optimizer.step()
    return model


def nearest_packet_object(state: ssrm.PhysicalState, objects: Sequence[ssrm.WorldObject]) -> Optional[ssrm.WorldObject]:
    active = [obj for obj in objects if obj.active]
    if not active:
        return None
    return min(active, key=lambda obj: ssrm.distance(state.x, state.z, obj.x, obj.z))


def choose_target(
    intent: str,
    state: ssrm.PhysicalState,
    packet: ssrm.PerceptionPacket,
    world: Dict[str, object],
    tick: int,
) -> Tuple[float, float]:
    return ssrm.target_for_intent(intent, state, packet, world, tick)


def run_learned_episode(
    model: PolicyNet,
    architecture: str,
    stage: ssrm.StageSpec,
    episode: int,
    cfg: LearnedControllerConfig,
    device: torch.device,
) -> Tuple[ControllerEvalRow, List[List[float]], List[List[float]], List[List[float]]]:
    rng = random.Random(ssrm.stable_seed(cfg.seed, "learned", architecture, stage.index, episode))
    world = ssrm.build_world(stage, episode + 10_000, ssrm.SSRM3DConfig(ticks=cfg.ticks, seed=cfg.seed, world_size=cfg.world_size))
    state = ssrm.PhysicalState(
        x=rng.uniform(-2.0, 2.0),
        z=rng.uniform(-2.0, 2.0),
        heading=rng.uniform(-math.pi, math.pi),
        energy=0.78,
        integrity=1.0,
        true_mobility=1.0,
        true_sensor=1.0,
    )
    packet = ssrm.perceive(state, world, stage, 0, ssrm.SSRM3DConfig(ticks=cfg.ticks, seed=cfg.seed, world_size=cfg.world_size), rng)
    perception_interval = max(1, round(60 / cfg.perception_hz))
    control_interval = 6
    previous_x = state.x
    previous_z = state.z
    previous_mode = "explore_reduce_uncertainty"
    intent = "collect_resource"
    hidden_state = model.initial_state(1, device)
    hidden_rows: List[List[float]] = []
    self_rows: List[List[float]] = []
    world_rows: List[List[float]] = []

    for tick in range(cfg.ticks):
        if not state.alive:
            break
        ssrm.update_competitor(world, ssrm.SSRM3DConfig(ticks=cfg.ticks, seed=cfg.seed, world_size=cfg.world_size), 1.0 / 60.0)
        if tick % perception_interval == 0:
            packet = ssrm.perceive(state, world, stage, tick, ssrm.SSRM3DConfig(ticks=cfg.ticks, seed=cfg.seed, world_size=cfg.world_size), rng)
        if stage.commitments and cfg.ticks * 0.32 < tick < cfg.ticks * 0.42:
            state.interrupted = True
        if tick % control_interval == 0:
            features, world_targets = feature_row(state, packet, world, stage, tick, cfg, previous_x, previous_z, previous_mode)
            input_row = torch.tensor([features], dtype=torch.float32, device=device)
            with torch.no_grad():
                hidden, logits, hidden_state = model.step(input_row, hidden_state)
            if packet.hazard_distance < 5.5:
                intent = "avoid_hazard"
            else:
                intent = MODES[int(torch.argmax(logits, dim=-1).item())]
                if intent == "honor_commitment" and not stage.commitments:
                    intent = "reach_shelter"
            hidden_rows.append([float(value) for value in hidden.detach().cpu().squeeze(0)])
            self_rows.append([state.energy, state.integrity, state.true_mobility, state.true_sensor])
            world_rows.append(world_targets)
            previous_mode = intent
        target_x, target_z = choose_target(intent, state, packet, world, tick)
        dx, dz = ssrm.unit_toward(state.x, state.z, target_x, target_z)
        terrain_drag = ssrm.clamp(
            abs(ssrm.terrain_height(state.x + dx, state.z + dz) - ssrm.terrain_height(state.x, state.z)) * 0.08,
            0.0,
            0.20,
        )
        speed_command = 5.2
        if intent in {"avoid_hazard", "honor_commitment", "reach_shelter"}:
            speed_command = 6.1
        actual_speed = speed_command * state.true_mobility * (0.55 + 0.45 * state.energy) * (1.0 - terrain_drag)
        previous_x, previous_z = state.x, state.z
        state.x = ssrm.clamp(state.x + dx * actual_speed / 60.0, -cfg.world_size * 0.5, cfg.world_size * 0.5)
        state.z = ssrm.clamp(state.z + dz * actual_speed / 60.0, -cfg.world_size * 0.5, cfg.world_size * 0.5)
        if abs(dx) + abs(dz) > EPS:
            state.heading = math.atan2(dz, dx)
        move_cost = 0.00009 * actual_speed + 0.00005 * stage.index
        if intent == "collect_resource" and stage.delayed_rewards:
            move_cost += 0.00009
        state.energy = ssrm.clamp(state.energy - move_cost, 0.0, 1.0)
        state.reward += 0.035
        ssrm.apply_stage_drift(state, stage, packet, tick, ssrm.SSRM3DConfig(ticks=cfg.ticks, seed=cfg.seed, world_size=cfg.world_size))
        ssrm.collect_resource(state, world)
        ssrm.apply_hazards(state, world, stage)
        ssrm.apply_shelter(state, packet, stage, tick, ssrm.SSRM3DConfig(ticks=cfg.ticks, seed=cfg.seed, world_size=cfg.world_size))
        if stage.commitments and intent in {"reach_shelter", "honor_commitment"}:
            shelter = nearest_packet_object(state, world["shelters"])
            if shelter and ssrm.distance(state.x, state.z, shelter.x, shelter.z) <= shelter.radius + 1.2 and tick > cfg.ticks * 0.40:
                state.commitment_done = True
                if state.interrupted:
                    state.recovered_after_interruption = True
        if stage.subsystem_conflict and intent == "collect_resource" and packet.hazard_distance < 10.0:
            state.reward -= 0.035 * stage.index
        if stage.multiagent and packet.competitor_distance < 8.0 and intent == "collect_resource":
            state.reward += 0.05
        if state.energy <= 0.001 or state.integrity <= 0.001:
            state.alive = False
            state.reward -= 75.0 + 12.0 * stage.index
    if stage.delayed_rewards:
        state.reward += 42.0 * state.energy + 38.0 * state.integrity
    if stage.commitments:
        if state.commitment_done:
            state.reward += 60.0 + 8.0 * stage.index
        else:
            state.reward -= 55.0 + 8.0 * stage.index
    if stage.subsystem_conflict:
        state.reward += 18.0 * min(state.energy, state.integrity)
    if stage.multiagent:
        state.reward += 8.0 if state.resources_collected >= 2 and state.energy > 0.35 else -8.0
    survived_ticks = min(cfg.ticks, max(0, tick + 1 if "tick" in locals() else 0))
    return (
        ControllerEvalRow(
            stage=stage.index,
            stage_name=stage.name,
            architecture=architecture,
            episode=episode,
            total_reward=state.reward,
            survived_ticks=survived_ticks,
            survival_fraction=survived_ticks / cfg.ticks,
            resources_collected=state.resources_collected,
            hazards_hit=state.hazards_hit,
            commitment_done=state.commitment_done,
            recovered_after_interruption=state.recovered_after_interruption,
        ),
        hidden_rows,
        self_rows,
        world_rows,
    )


def fit_probe(x: torch.Tensor, y: torch.Tensor, ridge: float) -> torch.Tensor:
    ones = torch.ones((x.shape[0], 1), dtype=x.dtype)
    design = torch.cat([x, ones], dim=1)
    penalty = torch.eye(design.shape[1], dtype=x.dtype) * ridge
    penalty[-1, -1] = 0.0
    return torch.linalg.solve(design.T @ design + penalty, design.T @ y)


def r2_score(prediction: torch.Tensor, target: torch.Tensor) -> float:
    scores: List[float] = []
    for column in range(target.shape[1]):
        y = target[:, column]
        variance = ((y - y.mean()) ** 2).sum()
        if float(variance) < 1e-8:
            continue
        residual = ((prediction[:, column] - y) ** 2).sum()
        scores.append(float(1.0 - residual / variance))
    if not scores:
        return 0.0
    return max(-1.0, min(1.0, statistics.fmean(scores)))


def probe_stats(
    hidden_rows: Sequence[List[float]],
    self_rows: Sequence[List[float]],
    world_rows: Sequence[List[float]],
    cfg: LearnedControllerConfig,
) -> Tuple[float, float, torch.Tensor]:
    if len(hidden_rows) < 12:
        hidden_dim = len(hidden_rows[0]) if hidden_rows else cfg.hidden_size
        return 0.0, 0.0, torch.zeros((hidden_dim + 1, 4), dtype=torch.float32)
    x = torch.tensor(hidden_rows, dtype=torch.float32)
    self_y = torch.tensor(self_rows, dtype=torch.float32)
    world_y = torch.tensor(world_rows, dtype=torch.float32)
    split = max(6, int(len(hidden_rows) * 0.55))
    self_probe = fit_probe(x[:split], self_y[:split], cfg.ridge)
    world_probe = fit_probe(x[:split], world_y[:split], cfg.ridge)
    ones = torch.ones((x[split:].shape[0], 1), dtype=torch.float32)
    design = torch.cat([x[split:], ones], dim=1)
    self_r2 = r2_score(design @ self_probe, self_y[split:])
    world_r2 = r2_score(design @ world_probe, world_y[split:])
    return self_r2, world_r2, self_probe


def self_edit_action_swing(model: PolicyNet, hidden_rows: Sequence[List[float]], self_probe: torch.Tensor, device: torch.device) -> float:
    if not hidden_rows or model.architecture == "frame_mlp":
        return 0.0
    directions = self_probe[:-1, :3]
    if torch.linalg.norm(directions) < 1e-8:
        return 0.0
    direction = 0.45 * directions[:, 0] + 0.35 * directions[:, 1] + 0.20 * directions[:, 2]
    direction = direction / (torch.linalg.norm(direction) + EPS)
    hidden = torch.tensor(hidden_rows, dtype=torch.float32, device=device)
    direction = direction.to(device)
    with torch.no_grad():
        low_logits = model.policy_head(hidden - 0.45 * direction)
        high_logits = model.policy_head(hidden + 0.45 * direction)
        low_probs = torch.softmax(low_logits, dim=-1)
        high_probs = torch.softmax(high_logits, dim=-1)
        swing = torch.abs(high_probs - low_probs).sum(dim=-1).mean()
    return float(swing.detach().cpu())


def evaluate_models(
    models: Dict[str, PolicyNet],
    cfg: LearnedControllerConfig,
    device: torch.device,
) -> Tuple[List[ControllerEvalRow], List[ControllerSummaryRow], List[ControllerVerdictRow], Dict[str, object]]:
    eval_rows: List[ControllerEvalRow] = []
    hidden_by_key: Dict[Tuple[int, str], List[List[float]]] = {}
    self_by_key: Dict[Tuple[int, str], List[List[float]]] = {}
    world_by_key: Dict[Tuple[int, str], List[List[float]]] = {}
    stages = [stage for stage in ssrm.STAGES if cfg.stage_min <= stage.index <= cfg.stage_max]
    for architecture, model in models.items():
        model.eval()
        for stage in stages:
            for episode in range(cfg.eval_episodes):
                row, hidden_rows, self_rows, world_rows = run_learned_episode(model, architecture, stage, episode, cfg, device)
                eval_rows.append(row)
                key = (stage.index, architecture)
                hidden_by_key.setdefault(key, []).extend(hidden_rows)
                self_by_key.setdefault(key, []).extend(self_rows)
                world_by_key.setdefault(key, []).extend(world_rows)
    frame_reward_by_stage: Dict[int, float] = {}
    frame_self_by_stage: Dict[int, float] = {}
    summary_rows: List[ControllerSummaryRow] = []
    for stage in stages:
        for architecture in ARCHITECTURES:
            rows = [row for row in eval_rows if row.stage == stage.index and row.architecture == architecture]
            key = (stage.index, architecture)
            self_r2, world_r2, self_probe = probe_stats(hidden_by_key.get(key, []), self_by_key.get(key, []), world_by_key.get(key, []), cfg)
            edit_swing = self_edit_action_swing(models[architecture], hidden_by_key.get(key, []), self_probe, device)
            mean_reward = statistics.fmean(row.total_reward for row in rows)
            if architecture == "frame_mlp":
                frame_reward_by_stage[stage.index] = mean_reward
                frame_self_by_stage[stage.index] = self_r2
            summary_rows.append(
                ControllerSummaryRow(
                    stage=stage.index,
                    stage_name=stage.name,
                    architecture=architecture,
                    mean_reward=mean_reward,
                    mean_survival_fraction=statistics.fmean(row.survival_fraction for row in rows),
                    mean_resources_collected=statistics.fmean(row.resources_collected for row in rows),
                    commitment_completion_rate=statistics.fmean(1.0 if row.commitment_done else 0.0 for row in rows),
                    recovery_rate=statistics.fmean(1.0 if row.recovered_after_interruption else 0.0 for row in rows),
                    self_probe_r2=self_r2,
                    world_probe_r2=world_r2,
                    self_edit_action_swing=edit_swing,
                    recurrent_reward_gain_over_frame=mean_reward - frame_reward_by_stage.get(stage.index, mean_reward),
                    recurrent_self_gain_over_frame=self_r2 - frame_self_by_stage.get(stage.index, self_r2),
                    device_used=str(device),
                    parameter_count=sum(parameter.numel() for parameter in models[architecture].parameters()),
                )
            )
    verdicts = build_verdicts(summary_rows)
    diagnostics = {
        "device": str(device),
        "torch_version": torch.__version__,
        "mps_available": torch.backends.mps.is_available(),
        "note": (
            "The controller is trained without self labels. Self-state is used only "
            "for post-training probes and counterfactual policy-state edits."
        ),
    }
    return eval_rows, summary_rows, verdicts, diagnostics


def build_verdicts(summary_rows: Sequence[ControllerSummaryRow]) -> List[ControllerVerdictRow]:
    verdicts: List[ControllerVerdictRow] = []
    for stage in ssrm.STAGES:
        rows = [row for row in summary_rows if row.stage == stage.index]
        if not rows:
            continue
        by_architecture = {row.architecture: row for row in rows}
        frame = by_architecture["frame_mlp"]
        recurrent_rows = [by_architecture[name] for name in RECURRENT_ARCHITECTURES]
        best = max(recurrent_rows, key=lambda row: (row.mean_reward, row.self_probe_r2, row.self_edit_action_swing))
        reward_gain = best.mean_reward - frame.mean_reward
        self_gain = best.self_probe_r2 - frame.self_probe_r2
        if stage.index == 0:
            supports = abs(reward_gain) < 20.0 and best.self_edit_action_swing < 0.12
            verdict = "learned_recurrence_not_needed_in_low_pressure_spatial_task" if supports else "stage_0_learned_control_not_clean"
        elif stage.index in {1, 2, 3}:
            supports = reward_gain > 8.0 and best.self_probe_r2 > 0.50 and self_gain > 0.05
            verdict = "learned_policy_state_decodes_self_and_improves_control" if supports else "early_pressure_learned_controller_not_sufficient"
        else:
            supports = reward_gain > 20.0 and best.self_probe_r2 > 0.50 and self_gain > 0.03
            verdict = "learned_policy_state_decodes_self_under_high_pressure" if supports else "high_pressure_learned_controller_not_sufficient"
        verdicts.append(
            ControllerVerdictRow(
                stage=stage.index,
                stage_name=stage.name,
                expected_pressure=stage.pressure,
                best_recurrent_architecture=best.architecture,
                frame_reward=frame.mean_reward,
                recurrent_reward=best.mean_reward,
                recurrent_reward_gain_over_frame=reward_gain,
                frame_self_r2=frame.self_probe_r2,
                recurrent_self_r2=best.self_probe_r2,
                recurrent_self_gain_over_frame=self_gain,
                recurrent_self_edit_action_swing=best.self_edit_action_swing,
                supports_learned_controller_precursor=supports,
                verdict=verdict,
            )
        )
    return verdicts


def run_experiment(
    cfg: LearnedControllerConfig,
) -> Tuple[List[ControllerEvalRow], List[ControllerSummaryRow], List[ControllerVerdictRow], Dict[str, object]]:
    random.seed(cfg.seed)
    torch.manual_seed(cfg.seed)
    device = resolve_device(cfg.device)
    records = generate_records(cfg)
    train_indices, test_indices = split_indices(records, cfg)
    tensors = build_tensors(records, device)
    models = {
        architecture: train_model(architecture, cfg, tensors, train_indices, device)
        for architecture in ARCHITECTURES
    }
    eval_rows, summary_rows, verdicts, diagnostics = evaluate_models(models, cfg, device)
    diagnostics.update(
        {
            "record_count": len(records),
            "train_sequences": len(train_indices),
            "test_sequences": len(test_indices),
            "eval_episodes_per_stage": cfg.eval_episodes,
            "source_agents": list(cfg.source_agents),
        }
    )
    return eval_rows, summary_rows, verdicts, diagnostics


def write_csv(path: Path, rows: Iterable[object]) -> None:
    rows = list(rows)
    if not rows:
        return
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=list(asdict(rows[0]).keys()))
        writer.writeheader()
        for row in rows:
            writer.writerow(asdict(row))


def print_table(verdicts: Sequence[ControllerVerdictRow]) -> None:
    headers = [
        "stage",
        "stage_name",
        "best_recurrent",
        "frame_reward",
        "recurrent_reward",
        "reward_gain",
        "recurrent_self_r2",
        "self_edit_action_swing",
        "supports_learned_controller_precursor",
    ]
    rows = []
    for verdict in verdicts:
        rows.append(
            [
                str(verdict.stage),
                verdict.stage_name,
                verdict.best_recurrent_architecture,
                f"{verdict.frame_reward:.3f}",
                f"{verdict.recurrent_reward:.3f}",
                f"{verdict.recurrent_reward_gain_over_frame:.3f}",
                f"{verdict.recurrent_self_r2:.3f}",
                f"{verdict.recurrent_self_edit_action_swing:.3f}",
                str(verdict.supports_learned_controller_precursor),
            ]
        )
    widths = [
        max(len(header), *(len(row[index]) for row in rows))
        for index, header in enumerate(headers)
    ]
    print(" | ".join(header.ljust(widths[index]) for index, header in enumerate(headers)))
    print("-+-".join("-" * width for width in widths))
    for row in rows:
        print(" | ".join(value.ljust(widths[index]) for index, value in enumerate(row)))


def parse_source_agents(value: str) -> Tuple[str, ...]:
    agents = tuple(item.strip() for item in value.split(",") if item.strip())
    unknown = [agent for agent in agents if agent not in ssrm.AGENTS]
    if unknown:
        raise argparse.ArgumentTypeError(f"unknown source agent(s): {', '.join(unknown)}")
    if not agents:
        raise argparse.ArgumentTypeError("at least one source agent is required")
    return agents


def parse_args() -> LearnedControllerConfig:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--episodes-per-stage", type=int, default=48)
    parser.add_argument("--eval-episodes", type=int, default=24)
    parser.add_argument("--ticks", type=int, default=540)
    parser.add_argument("--seed", type=int, default=20260609)
    parser.add_argument("--stage-min", type=int, default=0)
    parser.add_argument("--stage-max", type=int, default=6)
    parser.add_argument("--world-size", type=float, default=80.0)
    parser.add_argument("--perception-hz", type=int, default=10)
    parser.add_argument("--goal-hz", type=int, default=2)
    parser.add_argument("--reasoning-hz", type=float, default=0.5)
    parser.add_argument("--hidden-size", type=int, default=32)
    parser.add_argument("--epochs", type=int, default=160)
    parser.add_argument("--batch-size", type=int, default=64)
    parser.add_argument("--learning-rate", type=float, default=0.004)
    parser.add_argument("--train-fraction", type=float, default=0.72)
    parser.add_argument("--ridge", type=float, default=1e-3)
    parser.add_argument("--return-weight-floor", type=float, default=0.25)
    parser.add_argument("--value-weight", type=float, default=0.35)
    parser.add_argument("--device", choices=("auto", "mps", "cpu"), default="auto")
    parser.add_argument(
        "--source-agents",
        type=parse_source_agents,
        default=",".join(ssrm.AGENTS),
        help="comma-separated SSRM-3D trace-generating agents",
    )
    args = parser.parse_args()
    if args.episodes_per_stage < 8:
        raise SystemExit("--episodes-per-stage must be at least 8")
    if args.eval_episodes < 4:
        raise SystemExit("--eval-episodes must be at least 4")
    if args.ticks < 60:
        raise SystemExit("--ticks must be at least 60")
    if args.stage_min < 0 or args.stage_max > max(stage.index for stage in ssrm.STAGES):
        raise SystemExit("--stage-min/--stage-max out of range")
    if args.stage_min > args.stage_max:
        raise SystemExit("--stage-min must be <= --stage-max")
    if not 0.1 <= args.train_fraction <= 0.9:
        raise SystemExit("--train-fraction must be between 0.1 and 0.9")
    return LearnedControllerConfig(
        episodes_per_stage=args.episodes_per_stage,
        eval_episodes=args.eval_episodes,
        ticks=args.ticks,
        seed=args.seed,
        stage_min=args.stage_min,
        stage_max=args.stage_max,
        world_size=args.world_size,
        perception_hz=args.perception_hz,
        goal_hz=args.goal_hz,
        reasoning_hz=args.reasoning_hz,
        hidden_size=args.hidden_size,
        epochs=args.epochs,
        batch_size=args.batch_size,
        learning_rate=args.learning_rate,
        train_fraction=args.train_fraction,
        ridge=args.ridge,
        return_weight_floor=args.return_weight_floor,
        value_weight=args.value_weight,
        device=args.device,
        source_agents=args.source_agents,
    )


def main() -> int:
    cfg = parse_args()
    ARTIFACT_DIR.mkdir(parents=True, exist_ok=True)
    eval_rows, summary_rows, verdicts, diagnostics = run_experiment(cfg)
    summary_path = ARTIFACT_DIR / "ssrm_3d_learned_controller_summary.csv"
    eval_path = ARTIFACT_DIR / "ssrm_3d_learned_controller_eval.csv"
    verdict_path = ARTIFACT_DIR / "ssrm_3d_learned_controller_verdict.csv"
    results_path = ARTIFACT_DIR / "ssrm_3d_learned_controller_results.json"
    write_csv(summary_path, summary_rows)
    write_csv(eval_path, eval_rows)
    write_csv(verdict_path, verdicts)
    with results_path.open("w", encoding="utf-8") as handle:
        json.dump(
            {
                "config": {
                    **asdict(cfg),
                    "source_agents": list(cfg.source_agents),
                },
                "summary": [asdict(row) for row in summary_rows],
                "evaluation": [asdict(row) for row in eval_rows],
                "verdict": [asdict(row) for row in verdicts],
                "diagnostics": diagnostics,
            },
            handle,
            indent=2,
        )
        handle.write("\n")
    print(f"wrote {summary_path}")
    print(f"wrote {eval_path}")
    print(f"wrote {verdict_path}")
    print(f"wrote {results_path}")
    print(
        "device",
        diagnostics["device"],
        "torch",
        diagnostics["torch_version"],
        "mps_available",
        diagnostics["mps_available"],
    )
    print_table(verdicts)
    return 0 if all(row.supports_learned_controller_precursor for row in verdicts) else 1


if __name__ == "__main__":
    raise SystemExit(main())
