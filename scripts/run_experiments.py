def run_command(command: List[str]) -> Dict[str, object]:
    started = time.time()
    completed = subprocess.run(
        command,
        cwd=ROOT,
        check=False,
        text=True,
        capture_output=True,
    )
    elapsed = time.time() - started
    tail_lines = completed.stdout.strip().splitlines()[-12:]
    return {
        "command": command,
        "returncode": completed.returncode,
        "elapsed_seconds": elapsed,
        "stdout_tail": tail_lines,
        "stderr": completed.stderr.strip(),
    }


def main() -> int:
    ARTIFACT_DIR.mkdir(parents=True, exist_ok=True)
    runs = [run_command(command) for command in COMMANDS]
    manifest = {
        "status": "passed" if all(run["returncode"] == 0 for run in runs) else "failed",
        "runs": runs,
    }
    manifest_path = ARTIFACT_DIR / "experiment_manifest.json"
    with manifest_path.open("w", encoding="utf-8") as handle:
        json.dump(manifest, handle, indent=2)
        handle.write("\n")
    print(f"wrote {manifest_path}")
    return 0 if manifest["status"] == "passed" else 1


if __name__ == "__main__":
    raise SystemExit(main())
